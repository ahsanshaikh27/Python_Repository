# Assignment Operators Used to assign values to variables.
#  Operator	Example	   Equivalent to
#  =	     x = 5	         x = 5
#  +=	     x += 3	         x = x + 3
#  -=	     x -= 3	         x = x - 3  
#  *=	     x *= 3	         x = x * 3
#  /=	     x /= 3	         x = x / 3
#  %=	     x %= 3	         x = x % 3
#  //=	     x //= 3	     x = x // 3
#  **=	     x **= 3	     x = x ** 3

x = 10

x += 5  # x = x + 5
print(x)  # 15

x *= 2  # x = x * 2
print(x)  # 30